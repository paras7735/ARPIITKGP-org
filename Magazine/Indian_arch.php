<h5><b>INDIAN ARCH</b></h5>
<p>Indian Arch is a Annual Magazine under NASA(National Association of Students of Architecture) Banner that includes works and Writings of Revolutionary Architects associated with NASA. Also its a platform for Students to Contribute their best works as per the yearly theme. It also gives a glimpse of year long activities of NASA India that includes Zonal Conventions, Panel Discussions, NASA Day Activities ( Celebrated 13th September every year), LBC Workshops, Heritage Walks etc.
</p><br>

